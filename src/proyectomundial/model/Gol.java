package proyectomundial.model;

public class Gol {
    
    String partido;
    String gol;
    
    public Gol(String partido, String gol) {
        this.partido = partido;
        this.gol = gol;
    }

    public String getPartido() {
        return partido;
    }

    public String getGol() {
        return gol;
    }

}
